    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        Shoe shoe;
        String Mark;
        String color;
        int size;
        int price;
        boolean shoeMaterial;
        ShoeController shoeController = new ShoeController();

        Mark = txtMark.getText();
        color = cmbColor.getSelectedItem().toString();
        size = Integer.parseInt(txtSize.getText());
        price = Integer.parseInt(txtPrice.getText());
        shoeMaterial = Boolean.parseBoolean(cmbShoeMaterial.getSelectedItem().toString());

        shoe = new Shoe(Mark, color, size, price, shoeMaterial);

        shoeController.save(shoe);

        JOptionPane.showMessageDialog(rootPane, shoe.getMark());
    }//GEN-LAST:event_btnSaveActionPerformed

